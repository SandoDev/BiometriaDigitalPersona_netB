    public void setCourseGroups(List<CourseGroup> courses)
    {
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        for (CourseGroup course : courses) {
            model.addElement(course);
        }
        this.cmbCourseGroups.setModel(model);
    }

